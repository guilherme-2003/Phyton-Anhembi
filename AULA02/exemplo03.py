#saída de dados podem ser das seguintes formas:
nome = 'Gui'
print('Gui')
print(nome)

#da pra combinar literais e variáveis se ambas forem do tipo str
print('Meu nome é ' + nome)

#formas de separar variáveis de literais:
print('Meu nome é ' + str(nome)) #supondo que o literal e a variável não possuem o mesmo tipo
print('Meu nome é', nome) #vírgula é uma boa opção quando se tem poucas variáveis

